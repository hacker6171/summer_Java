public class ForLoopPractice {
    public static void Nested_Loop_Sample(char sysmbol,int rows,int columns)
    {
        for(int k=1;k<=rows;k++)
        {
            for(int i=1;i<=columns;i++)
            {
                System.out.print(sysmbol);
        
            }
            System.out.println();
        }
       
    }
}
